      roifilename = sprintf('%s_%s_%s.stl',filetemplate,'ES',templatename);
    end


    %Open file
    fid = fopen([pathname filesep roifilename],'w');
    if isequal(fid,-1)
      myfailed(dprintf('Could not open the file %s for writing.',[pathname filesep roifilename]));
      return;
    end

