function outdata = deleteemptycolumns(outdata,headerpos)
%-------------------------------------------
%Helper function to remove empty columns in exported strain data

% delete empty columns for slice values
header = outdata(headerpos,:);
foundcolumns = contains(header,dprintf('Slice'),'IgnoreCase',true);
indslice = find(foundcolumns);

if any(indslice,'all')
  for sl = indslice
    if ~all(cellfun('isempty',outdata(headerpos+1:end,sl)))
      foundcolumns(sl) = 0;
    end
  end
  columnstotake = not(foundcolumns);
  outdata = outdata(:,columnstotake);
end

% delete all columns with column header apex
header = outdata(headerpos,:);
foundcolumns = contains(header,'Apex','IgnoreCase',true);
indslice = find(foundcolumns);

if any(indslice,'all')
  columnstotake = not(foundcolumns);
  outdata = outdata(:,columnstotake);
end

