function utilityslicebasedvolumeexport_Callback
%----------------------------------------------
%Slice based volume export function for folder with mat files.
global DATA SET NO

%---Make sure workspace is cleared and ask user to save current file
docancel = asktosavecurrentfile;
if docancel
  return
end

pathname = DATA.Pref.datapath;
pathname = myuigetdir(pathname,'Select a folder with .mat files');
if isequal(pathname,0)
  return;
end

files2load = dir([pathname filesep '*.mat']);
numfiles = length(files2load);

if numfiles==0
  myfailed('No files to do area on found.');
  return;
end

outdata = cell(numfiles+1,120);

h = mywaitbarstart(numfiles,'Please wait, processing files.',3);
outdata{2,1} = 'Filename';
outdata{2,2} = 'SliceThickness';
outdata{2,3} = 'SliceGap';
outdata{2,4} = 'ResolutionX';
outdata{2,5} = 'ResolutionY';
outdata{2,6} = 'NoSlicesDrawn';
outdata{2,7} = 'Startslice';
outdata{2,8} = 'EndSlice';
outdata{1,9} = 'Epi volume (basal->apical) ED [ml]';
outdata{1,9+100} = 'Epi volume (basal->apical) ES [ml]';
outdata{1,9+200} = 'Endo volume (basal->apical) ED [ml]';
outdata{1,9+300} = 'Endo volume (basal->apical) ES [ml]';

%So we can write the headers correctly
maxnumslices=0;
for loop=1:numfiles

  %--- Load file
  DATA.Silent = true;
  fprintf('Loading %s\n.',files2load(loop).name);
  SET = []; %#ok<NASGU>
  load([pathname filesep files2load(loop).name],'-mat');
  SET = setstruct;
  clear setstruct;
  NO = 1; %#ok<NASGU>

  openfile('setupstacksfrommat',1);
  segment('renderstacksfrommat');

  %--- Find correct image stacks
  [cineno] = findfunctions('findcineshortaxisno');
  %[cineno] = findfunctions('findscarshortaxisno'); %Temporary change, change back!!!
  %cineno = 1;

  if isempty(cineno)
    [cineno] = findfunctions('findscarshortaxisno');
    disp('Could not find short axis cine stack. Taking scar instead.');
  end

  if isempty(cineno)
    disp('Could not find either cine or scar stack. Taking first stack.');
    cineno = 1;
  end

  NO = cineno;
  if SET(cineno).EDT==1 && SET(cineno).EST==1
    tools('autoesed_Callback');
  end

  %Get volume over the slices
  [L,E] = calcfunctions('calclvvolume',cineno);

  %Output general info
  outdata{loop+2,1} = files2load(loop).name;
  outdata{loop+2,2} = SET(cineno).SliceThickness;
  outdata{loop+2,3} = SET(cineno).SliceGap;
  outdata{loop+2,4} = SET(cineno).ResolutionX;
  outdata{loop+2,5} = SET(cineno).ResolutionY;
  outdata{loop+2,6} = size(L,1);
  endostartslice = 1000;
  epistartslice = 1000;
  if ~isempty(SET(cineno).EndoX)
    endostartslice = find(~isnan(SET(cineno).EndoX(1,SET(cineno).EDT,:)),1);
  elseif ~isempty(SET(cineno).EpiX)
    epistartslice = find(~isnan(SET(cineno).EpiX(1,SET(cineno).EDT,:)),1);
  end
  if isequal(endostartslice,1000) && isequal(epistartslice,1000)
    disp('No endo- or epicardium exist');
    temp = [];
  else
    [outdata{loop+2,7}, minindex] = min([endostartslice epistartslice]);
    if minindex == 1
      temp = find(~isnan(SET(cineno).EndoX(1,SET(cineno).EDT,:)));
    else
      temp = find(~isnan(SET(cineno).EpiX(1,SET(cineno).EDT,:)));
    end
  end

  if ~isempty(temp)
    outdata{loop+2,8} = temp(end);
  end

  if maxnumslices<=size(L,1)
    doslicenumheader = 1;
    maxnumslices = size(L,1);
  else
    doslicenumheader = 0;
  end

  for sloop=1:size(L,1)
    if isequal(SET(cineno).EDT,SET(cineno).EST)
      %EST & EDT same
      disp('Endsystole and enddiastole occur at the same time.');

      if doslicenumheader
        if sloop==1
          txt = 'Startslice';
        else
          txt = ['Startslice + ',num2str(sloop-1)];
        end
        outdata{2,8+sloop} = txt;
        outdata{2,8+100+sloop} = txt;
      end
      outdata{loop+2,8+sloop} = E(sloop,SET(cineno).EDT);
      outdata{loop+2,8+100+sloop} = L(sloop,SET(cineno).EDT);
    else

      if doslicenumheader
        if sloop==1
          txt = 'Startslice';
        else
          txt = ['Startslice + ',num2str(sloop-1)];
        end
        outdata{2,8+sloop} =  txt;
        outdata{2,8+100+sloop} =  txt;
        outdata{2,8+200+sloop} =  txt;
        outdata{2,8+300+sloop} =  txt;
      end


      outdata{loop+2,8+sloop} = E(sloop,SET(cineno).EDT);
      outdata{loop+2,8+100+sloop} = E(sloop,SET(cineno).EST);
      outdata{loop+2,8+200+sloop} = L(sloop,SET(cineno).EDT);
      outdata{loop+2,8+300+sloop} = L(sloop,SET(cineno).EST);
    end
  end
  h = mywaitbarupdate(h);
end
mywaitbarclose(h);

tmp={};
%make outdata dense
for i = 1:size(outdata,2)
  if ~all(cellfun(@isempty,outdata(:,i)))
    tmp=[tmp,outdata(:,i)];
  end
end

outdata=tmp;
segment('cell2clipboard',outdata);
segment('filecloseall_Callback',true);

DATA.Silent = false;


%----------------------------------
function exportslicevolume_Callback
%----------------------------------
%Export slicebased volume
global SET NO DATA

if SET(NO).Rotated
  myfailed('Rotated image stacks currently not supported for this operation.',DATA.GUI.Segment);
  return;
end

endo = true;
epi = true;
rvendo = true;
rvepi = true;

if isempty(SET(NO).EndoX)
  endo = false;
end
if isempty(SET(NO).EpiX)
  epi = false;
end
if isempty(SET(NO).RVEndoX)
  rvendo = false;
end
if isempty(SET(NO).RVEpiX)
  rvepi = false;
end

numcontours = endo+epi+rvendo+rvepi;

if (SET(NO).TSize>1)&&(SET(NO).Longaxis>1)
  mywarning('Automated long axis compensation not included in the export.',DATA.GUI.Segment);
end

%Reserve memory
outdata = cell(SET(NO).ZSize+8,1+SET(NO).TSize*numcontours);

%Write header
outdata{1,1} = 'FileName';
outdata{1,2} = SET(NO).FileName;
outdata{1,4} = 'Time';
outdata{1,5} = datestr(now);

%Export total volume
outdata{2,1} = 'TimeFrame';
outdata{3,1} = 'TotalEndo';
outdata{4,1} = 'TotalEpi';
outdata{5,1} = 'TotalRVEndo';
outdata{6,1} = 'TotalRVEpi';

for tloop=1:SET(NO).TSize
  outdata{2,1+tloop} = sprintf('%d',tloop);
  if endo
    outdata{3,1+tloop} = SET(NO).LVV(tloop);
  end
  if epi
    outdata{4,1+tloop} = SET(NO).EPV(tloop);
  end
  if rvendo
    outdata{5,1+tloop} = SET(NO).RVV(tloop);
  end
  if rvepi
    outdata{6,1+tloop} = SET(NO).RVEPV(tloop);
  end
end

%Find number of ROI's
temproinames=cell(1,SET(NO).RoiN);
for rloop=1:SET(NO).RoiN
  temproinames{rloop}=SET(NO).Roi(rloop).Name;
end
roinames = union(temproinames,{});

rowoffset = 8+length(roinames);

for zloop=1:SET(NO).ZSize
  outdata{rowoffset+zloop,1} = sprintf('Slice:%d',zloop);
end

coloffset = 1;
if endo
  outdata = exportslicehelper(outdata,rowoffset,coloffset,'Endo',...
    SET(NO).EndoX,SET(NO).EndoY);
  coloffset = coloffset+SET(NO).TSize;
end
if epi
  outdata = exportslicehelper(outdata,rowoffset,coloffset,'Epi',...
    SET(NO).EpiX,SET(NO).EpiY);
  coloffset = coloffset+SET(NO).TSize;
end
if rvendo
  outdata = exportslicehelper(outdata,rowoffset,coloffset,'RVEndo',...
    SET(NO).RVEndoX,SET(NO).RVEndoY);
  coloffset = coloffset+SET(NO).TSize;
end
if rvepi
  outdata = exportslicehelper(outdata,rowoffset,coloffset,'RVEpi',...
    SET(NO).RVEpiX,SET(NO).RVEpiY);
  coloffset = coloffset+SET(NO).TSize;
end

%Find ROI's
for loop=1:length(roinames)

  %Write ROI name
  outdata{6+loop,1} = roinames{loop};

  %Calculate volume for that ROI
  roivolume = nan(SET(NO).ZSize,SET(NO).TSize);
  for rloop=1:SET(NO).RoiN
    if isequal(SET(NO).Roi(rloop).Name,roinames{loop})
      for zloop=1:SET(NO).ZSize
        if ~isempty(find(SET(NO).Roi(rloop).Z==zloop,1))
          for tloop=1:SET(NO).TSize
            if ~isempty(find(SET(NO).Roi(rloop).T==tloop,1))
              temp = (SET(NO).SliceThickness+SET(NO).SliceGap)*stablepolyarea(...
                SET(NO).ResolutionY*SET(NO).Roi(rloop).Y(:,tloop),...
                SET(NO).ResolutionX*SET(NO).Roi(rloop).X(:,tloop))/1000;
              if ~isnan(temp)
                if isnan(roivolume(zloop,tloop))
                  roivolume(zloop,tloop) = 0;
                end
                roivolume(zloop,tloop) = roivolume(zloop,tloop)+temp;
              end
            end
          end
        end
      end
    end
  end

  %Export ROI volume
  temp = roivolume;
  temp(isnan(temp)) = 0;
  temp = sum(temp,1);

  for tloop=1:SET(NO).TSize
    outdata{rowoffset,tloop+coloffset} = sprintf('%s:TF%d',roinames{loop},tloop);
    outdata{6+loop,1+tloop} = temp(tloop);
  end
  for zloop=1:SET(NO).ZSize
    for tloop=1:SET(NO).TSize
      outdata{rowoffset+zloop,tloop+coloffset} = roivolume(zloop,tloop);
    end
  end

  %Increae column offset
  coloffset = coloffset+SET(NO).TSize;
end

segment('cell2clipboard',outdata);

%-------------------------------------------------------------------------
function outdata = exportslicehelper(outdata,rowoffset,coloffset,type,x,y)
%-------------------------------------------------------------------------
%Helper function to export slice based data.
global SET NO

for tloop=1:SET(NO).TSize
  outdata{rowoffset,tloop+coloffset} = sprintf('%s:TF%d',type,tloop);
end

for zloop=1:SET(NO).ZSize
  for tloop=1:SET(NO).TSize
    outdata{rowoffset+zloop,tloop+coloffset} = (SET(NO).SliceThickness+SET(NO).SliceGap)*stablepolyarea(...
      SET(NO).ResolutionY*y(1:end-1,tloop,zloop),...
      SET(NO).ResolutionX*x(1:end-1,tloop,zloop))/1000;
  end
end

