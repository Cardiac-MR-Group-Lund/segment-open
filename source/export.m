  %--- Load file
  DATA.Silent = true;
  mydisp(sprintf('Loading %s.',files2load(fileloop).name));

  %Make sure a fresh start
  SET = [];
  try
    %--- Load data from .mat file
    load([pathname filesep files2load(fileloop).name],'-mat');
    %Assign
    SET = setstruct;
    clear setstruct;
    NO = 1;
    %Call to intialize all variables correcly after loaded data.
    openfile('setupstacksfrommat',1);

    %--- Loop over stacks and write data
    foundgtdata = false;
    for loop = 1:length(SET)
      if isfield(SET(loop),'Gadgetron') && ~isempty(SET(loop).Gadgetron)
        foundgtdata = true;
        %Get data to export
        doheader = false;
        temp = exportgadgetrondata_Callback(doheader);
        %Copy data to the correct table
        if ~isempty(temp) && isfield(SET(loop).Gadgetron,'MBF')
          if numel(SET(loop).Gadgetron.MBF.mbf) < 32 %3 slices analysis
            %Write file name
            table3slc{numrows3slc+1,1} = files2load(fileloop).name;
            %Copy the data
            for col = 2:size(temp,2)
              for row = 1:size(temp,1)
                table3slc{numrows3slc+row,col} = temp{row,col};
              end
            end %copy data
            numrows3slc = numrows3slc + size(temp,1);
            break
          else %6 slices analysis
            %Write file name
            table6slc{numrows6slc+1,1} = files2load(fileloop).name;
            %Copy the data
            for col = 2:size(temp,2)
              for row = 1:size(temp,1)
                table6slc{numrows6slc+row,col} = temp{row,col};
              end
            end %copy data
            numrows6slc = numrows6slc + size(temp,1);
            break
          end %determine correct table
        end %find MBF data
      end %find Gadgetron data
    end %loop over stacks

    if ~foundgtdata
      fprintf('No Gadgetron data found in %s.\n',files2load(fileloop).name);
      continue %jump to next file
    end

  catch me
    %--- Something went wrong
    mydispexception(me);
    fprintf('Failed exporting data from %s.\n',files2load(fileloop).name);
  end

  h = mywaitbarupdate(h);

  %Make sure to close current dataset
  segment('filecloseall_Callback',true);

end %loop over files

%--- Concatenate the two tables
outdata = [table3slc; table6slc];

mywaitbarclose(h);

%--- Export table to clipboard
%If called with no output arguments then copy to clipboard.
if nargout==0
  segment('cell2clipboard',outdata);
else
  varargout{1} = outdata;
end

