function exportmultiple_Callback(dosegdicom)
%-------------------------------------------
%Creates summary of multiple matfiles in one folder
%This function is very useful for research. The user
%performs all delineations and then exports all data to
%one spread sheet.
global DATA SET NO

if nargin < 1
  dosegdicom = false;
end

%---Make sure workspace is cleared and ask user to save current file
docancel = asktosavecurrentfile;
if docancel
  return
end

%---Select path
if dosegdicom
  suffix = 'segdicom';
else
  suffix = 'mat';
end
pathname = DATA.Pref.datapath;
pathname = myuigetdir(pathname,sprintf('Select a folder with .%s files',suffix));
if isequal(pathname,0)
  return
end

%---Find files to process
files2load = dir([pathname filesep sprintf('*.%s',suffix)]);
numfiles = length(files2load);
if numfiles == 0
  myfailed('No files found.',DATA.GUI.Segment);
  return
end

%---Store the user's preferences
silentstate = DATA.Silent;

%--- Get user input
includenormalised = yesno('Do you want to include BSA normalized values?');

%---Loop over all files
h = mywaitbarstart(numfiles,'Please wait, loading and summarizing files.',1);
numrows = 1;
for fileloop = 1:numfiles
  %Load file
  DATA.Silent = true;
  filename = files2load(fileloop).name;
  fullfilename = fullfile(pathname,filename);
  fprintf('Loading %s.\n',filename);

  %---- try
  % This try-catch clause used to be commented out, which caused errors
  % in one file to crash the whole export process. Reintroducing it. /NL
  try
    %Load
    SET = []; %Make sure a fresh start
    if ~dosegdicom
      load(fullfilename,'-mat');
    else
      loader = segloader();
      loader.adddicomfiles({fullfilename});
      [~, setstruct] = loader.render(pathname, []);
      clear loader;
    end

    %Assign
    SET = setstruct;
    clear setstruct;
    NO = 1;

    %Call to intialize all variables correcly after loaded data.
    openfile('setupstacksfrommat',1);

    %Get data
    doheader = false; %header is added later for the whole table
    forceincludenormalised = true; %normalised value are removed later for the whole table, based on user input
    [tmpgeneral, tmproi, tmpmeas] = exportdata(doheader,forceincludenormalised,filename);

    %Copy the data
    for col = 1:size(tmpgeneral,2)
      for row = 1:size(tmpgeneral,1)
        tablegeneral{numrows,col} = tmpgeneral{row,col};
      end
    end

    for col = 1:size(tmproi,2)
      for row = 1:size(tmproi,1)
        tableroi{numrows,col} = tmproi{row,col};
      end
    end

    for col = 1:size(tmpmeas,2)
      for row = 1:size(tmpmeas,1)
        tablemeas{numrows,col} = tmpmeas{row,col};
      end
    end

  catch me
    %--- Something went wrong
    mydispexception(me);
    fprintf('Failed exporting data from %s.\n',filename);
    tablegeneral{numrows,1} = filename;
    tablegeneral{numrows,2} = 'FAILED.';
  end

  numrows = numrows + 1;

  h = mywaitbarupdate(h);

  %---Make sure to clear the workspace
  segment('filecloseall_Callback',true);

end %end of loop over files

%--- Remove normalised values
onlyone = false;
[headergeneral, headerroi, headermeas, indforbsa] = header(onlyone);
ind = true(1,size(tablegeneral,2));
if ~includenormalised
  ind(indforbsa) = false;
end
tablegeneral = tablegeneral(:,ind);
headergeneral = headergeneral(:,ind);

%--- Add headers to each table
tablegeneral = [headergeneral; tablegeneral];

if size(tableroi,2) > 4
  tmpheaderroi = [];
  for loop = 1:(size(tableroi,2)/4)
    tmpheaderroi = [tmpheaderroi headerroi]; %#ok<AGROW> removing MLINT warning since speed is not crucial here
  end
  headerroi = tmpheaderroi;
end
tableroi = [headerroi; tableroi];

if size(tablemeas,2) > 2
  tmpheadermeas = [];
  for loop = 1:(size(tablemeas,2)/2)
    tmpheadermeas = [tmpheadermeas headermeas]; %#ok<AGROW> removing MLINT warning since speed is not crucial here
  end
  headermeas = tmpheadermeas;
end
tablemeas = [headermeas; tablemeas];

%--- Concatenate tables
outdata = [tablegeneral, tableroi, tablemeas];

%--- Output to a string
mywaitbarclose(h);
segment('cell2clipboard',outdata);

%---Revert to user's preferences
DATA.Silent = silentstate;

