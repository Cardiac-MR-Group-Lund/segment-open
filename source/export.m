  %store file
  switch answ
    case stringyes
      segment('filesaveallas_Callback');
    case stringno
      % skip saving
      DATA.NeedToSave = false; %needed for 'filecloseall_Callback'
    otherwise
      % do nothing otherwise
      wascancelled = true;
      return
  end
  %close current file
  segment('filecloseall_Callback');
end

