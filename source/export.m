function [numfiles,files2load,dorecalc] = getfilestoloadstrainmitt(isbatch)
%-------------------------------------------
% get all files available for strain MITT export

numfiles = 0;
files2load = [];
dorecalc = false;

if isbatch
  %---Perform preprocessing tasks
  %Ask user to save current file
  docancel = asktosavecurrentfile;
  if docancel
    return
  end   

  %Ask user input
  questn = sprintf('%s\n%s',...
    'During batch export, if segmentation is changed in a file and thereby',...
    'strain need to be calculated, the file will be excluded from the export.');
  answ = myquestdlg(questn,{'Continue','Cancel now'},'Continue');

  switch lower(answ)
    case 'recalculate'
      dorecalc = true;
    case 'continue'
      dorecalc = false;
    otherwise
      return
  end


  %Get files to export
  [files2load,numfiles] = selectfolderpath;
  if numfiles == 0
    return
  end

else
  numfiles = 1;
  myworkon
end
