function varargout = exportmultiplegadgetrondata_Callback
%-------------------------------------------
%Export Gadgetron analysis from multiple .mat files in the same folder.
%Callback from Segment and Segment CMR Utility menu.
global DATA SET NO

%---Make sure workspace is cleared and ask user to save current file
docancel = asktosavecurrentfile;
if docancel
  return
end

%--- Select path
suffix = 'mat';
pathname = DATA.Pref.datapath;
pathname = myuigetdir(pathname,sprintf('Select a folder with .%s files',suffix));
if isequal(pathname,0)
  return;
end

%--- Find files to process
files2load = dir([pathname filesep sprintf('*.%s',suffix)]);
numfiles = length(files2load);

if numfiles == 0
  myfailed('Found no files to summarize.',DATA.GUI.Segment);
  return;
end

%--- Create output matrices
%Gadgetron analysis can be performed on either 3 or 6 slices. For 3 slices
%acquisitions, we get 16 values of MBF; for 6 slices acquistions, we get 32
%values of MBF. In case the two types of acquisitions are blending in the
%same folder, we need to group the files acquired for 3 slices and those
%acquired for 6 slices in two different tables, since they won't have the
%same header.
table3slc = cell(numfiles*2+1,50); %*2 for stress and rest; +1 for header
table6slc = cell(numfiles*2+1,50); %*2 for stress and rest; +1 for header
numrows3slc = 1;
