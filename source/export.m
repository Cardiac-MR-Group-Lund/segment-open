function recalculationstate = recalculatestrainmitt(heartpartstoredo, heartpart, no, imageviewplane)
%-------------------------------------------
% function to start recalculation of strain MITT before xporting the values
recalculationstate = 'no';
if any(~cellfun('isempty',heartpartstoredo),'all') && any(contains(heartpartstoredo,heartpart))
  % marked for recalculation
  try
    strainmitt.strainmitt('analysis_helper',no,imageviewplane,heartpart);
    recalculationstate = 'yes';
  catch me
    mydispexception(me);
    recalculationstate = 'failed';
  end
end
