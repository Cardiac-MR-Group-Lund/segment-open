for sliceloop = 1:numslices
  slicestr = dprintf('Slice %d',sliceloop);
  incr = sliceloop-1;
  outdata{rowind,columnind+incr} = slicestr;
end

%-------------------------------------------
function outdata = exportstrainmittsax(isbatch,nos)
%-------------------------------------------
% function to export strain mitt results for SAX
global DATA SET

[numfiles,files2load,~] = getfilestoloadstrainmitt(isbatch);
%Create output matrix
outdata = cell(1,1);
if numfiles == 0
  return
end

if isbatch
  h = mywaitbarstart(numfiles,'Please wait, loading and summarizing files.',1);
else
  myworkon;
end

%---Write header using writeheader function (see example below)
% [outdata,headercolind,colind] = writeheader(outdata,headerstr,rowind,colind)
outdata{1,1} = dprintf('Results from Strain MITT');
rowind = 3;

rowoffset = 1;
startcolumn = 1;
strainunit = '%';
timeunit = 'ms';
strainrateunit = '1/s';
viewplanetype = 'SAX';

%% Patient info
if isbatch
  [outdata,colfilename] = writeheader(outdata,dprintf('File name'),rowind,startcolumn);
  [outdata,colrecalc] = writeheader(outdata,dprintf('Recalculated'),rowind);
  startcolumn = size(outdata,2)+1;
end
[outdata,colpatname] = writeheader(outdata,dprintf('Patient name'),rowind,startcolumn);
[outdata,colpatid] = writeheader(outdata,dprintf('Patient ID'),rowind);
[outdata,colacqdate] = writeheader(outdata,dprintf('Acquisition date'),rowind);
[outdata,colhr] = writeheader(outdata,makeunitstring(dprintf('Heart Rate'),'bmp'),rowind);

[outdata,coldatatypenum] = writeheader(outdata,dprintf('Data type'),rowind);
[outdata,colstacknum] = writeheader(outdata,dprintf('Stack'),rowind);
[outdata,colimgtype] = writeheader(outdata,dprintf('Image Type'),rowind);

[outdata,colestf] = writeheader(outdata,makeunitstring(dprintf('ES Time'),timeunit),rowind);
lvstr = dprintf('LV');
lvheaderstr = sprintf('%s %s',lvstr,dprintf('Peak Time'));
lvheaderstr = makeunitstring(lvheaderstr,timeunit);
[outdata,collvpeaktf] = writeheader(outdata,lvheaderstr,rowind);

% write slice position such as apical/midventricular/basal for each slice
[outdata,colsliceposlabel] = writeheader(outdata,dprintf('Slice position label'),rowind-rowoffset);
outdata = writeslicesnamesinsax(outdata,rowind,colsliceposlabel);

%% LV Global strain values
lvheaderstr = sprintf('%s %s',lvstr,dprintf('GRS'));
lvglobalradstr = makeunitstring(lvheaderstr,strainunit);
[outdata,collvglobalrad] = writeheader(outdata,lvglobalradstr,rowind);

lvheaderstr = sprintf('%s %s',lvstr,dprintf('GCS'));
lvglobaltangentstr = makeunitstring(lvheaderstr,strainunit);
[outdata,collvglobaltangent] = writeheader(outdata,lvglobaltangentstr,rowind);

%% LV Global strain values per slice
[outdata,collvglobalradslice] = writeheader(outdata,lvglobalradstr,rowind-rowoffset);
outdata = writeslicesnamesinsax(outdata,rowind,collvglobalradslice);

[outdata,collvglobaltangentslice] = writeheader(outdata,lvglobaltangentstr,rowind-rowoffset);
outdata = writeslicesnamesinsax(outdata,rowind,collvglobaltangentslice);

%% LV strain values per sector
lvpeakradialstr = sprintf('%s %s',lvstr,dprintf('Peak radial strain'));
lvpeakradialstr = makeunitstring(lvpeakradialstr,strainunit); % LV Peak radial strain [%]
[outdata,colpeakrad] = writeheader(outdata,lvpeakradialstr,rowind-rowoffset);
outdata = writeahasectorsnames(outdata,rowind,colpeakrad); %AHA sectors

lvpeaktangentstr = sprintf('%s %s',lvstr,dprintf('Peak circumferential strain'));
lvpeaktangentstr = makeunitstring(lvpeaktangentstr,strainunit); % LV Peak circumferential strain [%]
[outdata,colpeaktangent] = writeheader(outdata,lvpeaktangentstr,rowind-rowoffset);
outdata = writeahasectorsnames(outdata,rowind,colpeaktangent); %AHA sectors

%% LV strain rate global

lvheaderstr = dprintf('LV Radial Strain Rate'); % LV Peak radial strain
[outdata,collvradsr] = writeheader(outdata,lvheaderstr,rowind-rowoffset);
outdata = writepeakheaders(outdata,collvradsr,rowind); % Peak Systole [1/s]	Peak Systole Time [ms]	Peak Diastole [1/s]	Peak Diastole Time [ms]

lvheadersr = dprintf('LV Circumferential Strain Rate'); % LV Peak radial strain
[outdata,collvtangentsr] = writeheader(outdata,lvheadersr,rowind-rowoffset);
outdata = writepeakheaders(outdata,collvtangentsr,rowind); % Peak Systole [1/s]	Peak Systole Time [ms]	Peak Diastole [1/s]	Peak Diastole Time [ms]

%% LV Radial strain rate per slice
lvheaderstr = makeunitstring(dprintf('LV Radial strain rate per slice at local peak systole'),strainrateunit);
[outdata,collvradsrviewpeaksys] = writeheader(outdata,lvheaderstr,rowind-rowoffset);
outdata = writeslicesnamesinsax(outdata,rowind,collvradsrviewpeaksys);

lvheaderstr = makeunitstring(dprintf('LV Radial strain rate per slice local peak systole time'),timeunit);
[outdata,collvradsrviewpeaksystime] = writeheader(outdata,lvheaderstr,rowind-rowoffset);
outdata = writeslicesnamesinsax(outdata,rowind,collvradsrviewpeaksystime);

lvheaderstr = makeunitstring(dprintf('LV Radial strain rate per slice at local peak diastole'),strainrateunit);
[outdata,collvradsrviewpeakdias] = writeheader(outdata,lvheaderstr,rowind-rowoffset);
outdata = writeslicesnamesinsax(outdata,rowind,collvradsrviewpeakdias);

lvheaderstr = makeunitstring(dprintf('LV Radial strain rate per slice local peak diastole time'),timeunit);
[outdata,collvradsrviewpeakdiastime] = writeheader(outdata,lvheaderstr,rowind-rowoffset);
outdata = writeslicesnamesinsax(outdata,rowind,collvradsrviewpeakdiastime);

%% LV longitudinal strain rate per slice
lvheaderstr = makeunitstring(dprintf('LV Circumferential strain rate per slice at local peak systole'),strainrateunit);
[outdata,collvtangentsrviewpeaksys] = writeheader(outdata,lvheaderstr,rowind-rowoffset);
outdata = writeslicesnamesinsax(outdata,rowind,collvtangentsrviewpeaksys);

lvheaderstr = makeunitstring(dprintf('LV Circumferential strain rate per slice local peak systole time'),timeunit);
[outdata,collvtangentsrviewpeaksystime] = writeheader(outdata,lvheaderstr,rowind-rowoffset);
outdata = writeslicesnamesinsax(outdata,rowind,collvtangentsrviewpeaksystime);

lvheaderstr = makeunitstring(dprintf('LV Circumferential strain rate per slice at local peak diastole'),strainrateunit);
[outdata,collvtangentsrviewpeakdias] = writeheader(outdata,lvheaderstr,rowind-rowoffset);
outdata = writeslicesnamesinsax(outdata,rowind,collvtangentsrviewpeakdias);

lvheaderstr = makeunitstring(dprintf('LV Circumferential strain rate per slice local peak diastole time'),timeunit);
[outdata,collvtangentsrviewpeakdiastime] = writeheader(outdata,lvheaderstr,rowind-rowoffset);
outdata = writeslicesnamesinsax(outdata,rowind,collvtangentsrviewpeakdiastime);

%% LV Radial strain rate per sector
lvheaderstr = makeunitstring(dprintf('LV Radial strain rate per sector at local peak systole'),strainrateunit);
[outdata,collvradsrsectorpeaksys] = writeheader(outdata,lvheaderstr,rowind-rowoffset);
outdata = writeahasectorsnames(outdata,rowind,collvradsrsectorpeaksys); %AHA sectors

lvheaderstr = makeunitstring(dprintf('LV Radial strain rate per sector local peak systole time'),timeunit);
[outdata,collvradsrsectorpeaksystime] = writeheader(outdata,lvheaderstr,rowind-rowoffset);
outdata = writeahasectorsnames(outdata,rowind,collvradsrsectorpeaksystime);

lvheaderstr = makeunitstring(dprintf('LV Radial strain rate per sector at local peak diastole'),strainrateunit);
[outdata,collvradsrsectorpeakdias] = writeheader(outdata,lvheaderstr,rowind-rowoffset);
outdata = writeahasectorsnames(outdata,rowind,collvradsrsectorpeakdias);

lvheaderstr = makeunitstring(dprintf('LV Radial strain rate per sector local peak diastole time'),timeunit);
[outdata,collvradsrsectorpeakdiastime] = writeheader(outdata,lvheaderstr,rowind-rowoffset);
outdata = writeahasectorsnames(outdata,rowind,collvradsrsectorpeakdiastime);

%% LV Longitudinal strain rate per sector
lvheaderstr = makeunitstring(dprintf('LV Circumferential strain rate per sector at local peak systole'),strainrateunit);
[outdata,collvtangentsrsectorpeaksys] = writeheader(outdata,lvheaderstr,rowind-rowoffset);
outdata = writeahasectorsnames(outdata,rowind,collvtangentsrsectorpeaksys); %AHA sectors

lvheaderstr = makeunitstring(dprintf('LV Circumferential strain rate per sector local peak systole time'),timeunit);
[outdata,collvtangentsrsectorpeaksystime] = writeheader(outdata,lvheaderstr,rowind-rowoffset);
outdata = writeahasectorsnames(outdata,rowind,collvtangentsrsectorpeaksystime);

lvheaderstr = makeunitstring(dprintf('LV Circumferential strain rate per sector at local peak diastole'),strainrateunit);
[outdata,collvtangentsrsectorpeakdias] = writeheader(outdata,lvheaderstr,rowind-rowoffset);
outdata = writeahasectorsnames(outdata,rowind,collvtangentsrsectorpeakdias);

lvheaderstr = makeunitstring(dprintf('LV Circumferential strain rate per sector local peak diastole time'),timeunit);
[outdata,collvtangentsrsectorpeakdiastime] = writeheader(outdata,lvheaderstr,rowind-rowoffset);
outdata = writeahasectorsnames(outdata,rowind,collvtangentsrsectorpeakdiastime);

%% RV Global strain values
rvstr = dprintf('RV');
rvheaderstr = sprintf('%s %s',rvstr,dprintf('Peak Time'));
rvheaderstr = makeunitstring(rvheaderstr,timeunit);
[outdata,colrvpeaktf] = writeheader(outdata,rvheaderstr,rowind);

rvheaderstr = sprintf('%s %s',rvstr,dprintf('GCS'));
rvheaderstr = makeunitstring(rvheaderstr,strainunit); % RV GCS [%]
[outdata,colrvglobaltangent] = writeheader(outdata,rvheaderstr,rowind);

[outdata,colrvglobaltangentslice] = writeheader(outdata,rvheaderstr,rowind-rowoffset);
outdata = writeslicesnamesinsax(outdata,rowind,colrvglobaltangentslice);

%% RV Global septal and lateral strain

rvheaderstr = sprintf('%s %s',rvstr,dprintf('Lateral Strain'));
rvheaderstr = makeunitstring(rvheaderstr,strainunit); % RV Lateral Strain [%]
[outdata,colrvlateraltangent] = writeheader(outdata,rvheaderstr,rowind);

[outdata,colrvlateraltangentslice] = writeheader(outdata,rvheaderstr,rowind-rowoffset);
outdata = writeslicesnamesinsax(outdata,rowind,colrvlateraltangentslice);

rvheaderstr = sprintf('%s %s',rvstr,dprintf('Septal Strain'));
rvheaderstr = makeunitstring(rvheaderstr,strainunit); % RV Septal Strain [%]
[outdata,colrvseptaltangent] = writeheader(outdata,rvheaderstr,rowind);

[outdata,colrvseptaltangentslice] = writeheader(outdata,rvheaderstr,rowind-rowoffset);
outdata = writeslicesnamesinsax(outdata,rowind,colrvseptaltangentslice);

%%
heartpartstodo = {'LV','RV'};
%---Loop over all files
for fileloop = 1:numfiles
  %Load file
  if isbatch
    DATA.Silent = true; %Turn on "silent" mode to avoid to much update on screen when loading etc.
    loadsetstruct(files2load(fileloop).folder,files2load(fileloop).name);
    if isempty(SET) %-> no data was loaded skip this file
      continue
    end
    nos = 1:length(SET);
  end

  %-- loop over all nos
  for no = nos
    if ~isempty(SET(no).StrainMitt)
      imageviewplane = SET(no).StrainMitt.ImageViewPlane;
      if ~strcmpi(imageviewplane,viewplanetype)
        continue %skip this stack
      end

      existingheartparts = getheartparts(SET(no).StrainMitt);

      if any(contains(existingheartparts,heartpartstodo))
        % increment row index
        rowind = rowind + 1;

        % fill in patient info
        outdata{rowind,colpatname} = SET(no).PatientInfo.Name;
        outdata{rowind,colpatid} = SET(no).PatientInfo.ID;
        outdata{rowind,colacqdate} = SET(no).PatientInfo.AcquisitionDate;
        outdata{rowind,coldatatypenum} = segment('getcurrentdatatypedescription',no);

        if isbatch
          outdata{rowind,colfilename} = files2load(fileloop).name;
          % check if the file needs to be skipped
          heartpartstoredo = needredo(SET(no).StrainMitt);
          if any(~cellfun('isempty',heartpartstoredo)) % check for non-empty parts
            % if there are any parts that need to be recalculated then skip this no
            outdata{rowind,colrecalc} = 'SKIPPED';
            continue
          else
            outdata{rowind,colrecalc} = 'OK';
          end
        end
        % get time vector (needed for ES and peak tf)
        tvector = strainmitt.strainguifunctions('gettimevectorinms',no,viewplanetype);
        % write heart rate and ES time for no
        outdata{rowind,colhr} = SET(no).HeartRate;
        outdata{rowind,colstacknum} = num2str(no);
        outdata{rowind,colimgtype} = sprintf('%s %s',SET(no).ImageType,SET(no).ImageViewPlane);
        outdata{rowind,colestf} = tvector(SET(no).EST);

        %-- LV export
        if any(contains(existingheartparts,'LV'))
          lvstrainresult = SET(no).StrainMitt.(viewplanetype).LV.Result;
          % write peaktime for LV
          lvpeaktime = getpeaktf(lvstrainresult);
          outdata{rowind,collvpeaktf} = tvector(lvpeaktime);

          % write slice position names Basal/mid/apical
          slicelabels = lvstrainresult.SliceLabels;
          numslices = numel(slicelabels);
          for sliceloop = 1:numslices
            incr = sliceloop-1;
            saslicetext = strainmitt.strainmitt('getfullslicedesciption',slicelabels{sliceloop});
            outdata{rowind,colsliceposlabel+incr} = saslicetext;
          end

          % GRS
          outdata{rowind,collvglobalrad} = getpeakmeanglobalstrainrad(lvstrainresult);
          % GCS
          outdata{rowind,collvglobaltangent} = getpeakmeanglobalstraintangent(lvstrainresult);
          
          % global LV strain per slice
          strainrad = getglobalstrain(lvstrainresult,'radial');
          straintangent = getglobalstrain(lvstrainresult,'tangent');
          for sliceloop = 1:numslices
            incr = sliceloop-1;
            outdata{rowind,collvglobalradslice+incr} = strainrad(lvpeaktime,sliceloop);
            outdata{rowind,collvglobaltangentslice+incr} = straintangent(lvpeaktime,sliceloop);
          end

          % LV Peak strain per sector
          for cloop = [colpeakrad,colpeaktangent]
            switch cloop
              case colpeakrad
                straintype = 'radial';
              case colpeaktangent
                straintype = 'tangent';
            end
            bullseyeplot = strainmitt.strainmitt('bullseye_helper',no,straintype,imageviewplane);
            outdata = writebullseyevalues(outdata,bullseyeplot,rowind,cloop);
          end

          % write global strain rate
          for cloop = [collvradsr,collvtangentsr]
            switch cloop
              case collvradsr
                straintype = 'radial';
              case collvtangentsr
                straintype = 'circ';
            end

            peaktype = 'mean';
            [peaksysvalue, peaksystime, peakdiasvalue, peakdiastime] = strainmitt.strainrate('getlvstrainratepeaksforreport',no,viewplanetype,straintype,peaktype);

            outdata{rowind,cloop} = peaksysvalue;
            outdata{rowind,cloop+1} = peaksystime;
            outdata{rowind,cloop+2} = peakdiasvalue;
            outdata{rowind,cloop+3} = peakdiastime;
          end

          % strainrate per view
          peaktype = 'slice';
          straintype = 'tangent';
          [peaksysvalue, peaksystime, peakdiasvalue, peakdiastime] = strainmitt.strainrate('getlvstrainratepeaksforreport',no,viewplanetype,straintype,peaktype);
          if ~isempty(peaksystime) && ~isempty(peakdiastime)
            for sliceloop = 1:numslices
              incr = sliceloop-1;
              sysvalue = peaksysvalue(sliceloop);
              outdata{rowind,collvtangentsrviewpeaksys+incr} = sysvalue;
              if ~isnan(sysvalue)
                outdata{rowind,collvtangentsrviewpeaksystime+incr} = peaksystime(sliceloop);
              end
              diasvalue = peakdiasvalue(sliceloop);
              outdata{rowind,collvtangentsrviewpeakdias+incr} = diasvalue;
              if ~isnan(diasvalue)
                outdata{rowind,collvtangentsrviewpeakdiastime+incr} = peakdiastime(sliceloop);
              end
            end
          end

          straintype = 'radial';
          [peaksysvalue, peaksystime, peakdiasvalue, peakdiastime] = strainmitt.strainrate('getlvstrainratepeaksforreport',no,viewplanetype,straintype,peaktype);
          if ~isempty(peaksystime) && ~isempty(peakdiastime)
            for sliceloop = 1:numslices
              incr = sliceloop-1;
              sysvalue = peaksysvalue(sliceloop);
              outdata{rowind,collvradsrviewpeaksys+incr} = sysvalue;
              if ~isnan(sysvalue)
                outdata{rowind,collvradsrviewpeaksystime+incr} = peaksystime(sliceloop);
              end
              diasvalue = peakdiasvalue(sliceloop);
              outdata{rowind,collvradsrviewpeakdias+incr} = diasvalue;
              if ~isnan(diasvalue)
                outdata{rowind,collvradsrviewpeakdiastime+incr} = peakdiastime(sliceloop);
              end
            end
          end

          % strainrate per sector
          peaktype = 'lvsegmental';
          numsectors = 16; 
          straintype = 'tangent';
          [peaksysvalue, peaksystime, peakdiasvalue, peakdiastime] = strainmitt.strainrate('getlvstrainratepeaksforreport',no,viewplanetype,straintype,peaktype);
          if ~isempty(peaksystime) && ~isempty(peakdiastime)
            for sectorloop = 1:numsectors
              incr = sectorloop-1; % minus 1 since first column is for sector 1
              sysvalue = peaksysvalue(sectorloop);
              outdata{rowind,collvtangentsrsectorpeaksys+incr} = sysvalue;
              if ~isnan(sysvalue)
                outdata{rowind,collvtangentsrsectorpeaksystime+incr} = peaksystime(sectorloop);
              end
              diasvalue = peakdiasvalue(sectorloop);
              outdata{rowind,collvtangentsrsectorpeakdias+incr} = diasvalue;
              if ~isnan(diasvalue)
                outdata{rowind,collvtangentsrsectorpeakdiastime+incr} = peakdiastime(sectorloop);
              end
            end
          end

          straintype = 'radial';
          [peaksysvalue, peaksystime, peakdiasvalue, peakdiastime] = strainmitt.strainrate('getlvstrainratepeaksforreport',no,viewplanetype,straintype,peaktype);
          if ~isempty(peaksystime) && ~isempty(peakdiastime)
            for sectorloop = 1:numsectors
              incr = sectorloop-1;
              sysvalue = peaksysvalue(sectorloop);
              outdata{rowind,collvradsrsectorpeaksys+incr} = sysvalue;
              if ~isnan(sysvalue)
                outdata{rowind,collvradsrsectorpeaksystime+incr} = peaksystime(sectorloop);
              end
              diasvalue = peakdiasvalue(sectorloop);
              outdata{rowind,collvradsrsectorpeakdias+incr} = diasvalue;
              if ~isnan(diasvalue)
                outdata{rowind,collvradsrsectorpeakdiastime+incr} = peakdiastime(sectorloop);
              end
            end
          end

        end

        %-- RV export
        if any(contains(existingheartparts,'RV'))
          %RV exist in 4CH
          rvstrainresult = strainmitt.strainmitt('getstrainresultforno',no,'RV');
          if ~isempty(rvstrainresult)
            rvpeaktime = getpeaktf(rvstrainresult);
            outdata{rowind,colrvpeaktf} = tvector(rvpeaktime);

            % global strain
            outdata{rowind,colrvglobaltangent} = getpeakmeanglobalstraintangent(rvstrainresult);

            % check if slice labels were written
            if ~exist('slicelabels','var')
              % write slice position names Basal/mid/apical
              slicelabels = rvstrainresult.SliceLabels;
              numslices = numel(slicelabels);
              for sliceloop = 1:numslices
                incr = sliceloop-1;
                saslicetext = strainmitt.strainmitt('getfullslicedesciption',slicelabels{sliceloop});
                outdata{rowind,colsliceposlabel+incr} = saslicetext;
              end
            end

            % RV global strain slicewise
            straintangent = getglobalstrain(rvstrainresult,'tangent');
            for sliceloop = 1:numslices
              incr = sliceloop-1;
              outdata{rowind,colrvglobaltangentslice+incr} = straintangent(rvpeaktime,sliceloop);
            end

            meansectortangent = getmeansectorstraintangent(rvstrainresult);
            outdata{rowind,colrvlateraltangent} = meansectortangent(1,rvpeaktime);
            outdata{rowind,colrvseptaltangent} = meansectortangent(2,rvpeaktime);

            [rvsectorlateral,rvsectorseptum] = getsectorlateralseptum(rvstrainresult);
            if isrow(rvsectorlateral)
              % make sure column vector
              rvsectorlateral = rvsectorlateral';
            end
            if isrow(rvsectorseptum)
              % make sure column vector
              rvsectorseptum = rvsectorseptum';
            end
            for sliceloop = 1:numslices
              incr = sliceloop-1;
              outdata{rowind,colrvlateraltangentslice+incr} = rvsectorlateral(rvpeaktime,sliceloop);
              outdata{rowind,colrvseptaltangentslice+incr} = rvsectorseptum(rvpeaktime,sliceloop);
            end
          end
        end %end RV
      end
    end %end strain export for current no
  end %end loop over nos

  if isbatch
    h = mywaitbarupdate(h);
    %close current file
    segment('filecloseall_Callback');
  end

end %end loop over files

if isbatch
  mywaitbarclose(h);
else
  myworkoff
end

%-------------------------------------------
function exportmultiplestrain_Callback(type,batchcall) %#ok<INUSD> 
%-------------------------------------------
%Creates summary of strain result (tagging or cine strain analysis)
%from multiple matfiles in one folder.
%This function is very useful for research. The user
%performs all strain analysis and then exports all data to
%one spreadsheet.

global DATA SET NO

if nargin < 2
  batchcall = false;
end


doTaggroup = true;  % Correct issue with taggroup if any long-axis image have been deleted from the file

suffix = 'mat';

%---Make sure workspace is cleared and ask user to save current file
docancel = asktosavecurrentfile;
if docancel
  return
end

%Select path
pathname = DATA.Pref.datapath;
pathname = myuigetdir(pathname,sprintf('Select a folder with .%s files',suffix));
if isequal(pathname,0)
  %   myfailed('Aborted.',DATA.GUI.Segment);
  return;
end

%Find files to process
files2load = dir([pathname filesep sprintf('*.%s',suffix)]);
numfiles = length(files2load);

if numfiles==0
  myfailed('Found no files to summarize.',DATA.GUI.Segment);
  return;
end

% do strain?
output=questdlg('Do you wish to redo strain analysis?');
switch output
  case 'Yes'
    doStrain = 1;
    % autoset peaktf?
    output = questdlg('Do you wish to autodetect peak time frame?');
    switch output
      case 'Yes'
        doPeaktf = 1;
      case 'No'
        doPeaktf = 0;
      case 'Cancel'
        return;
    end
  case 'No'
    doStrain = 0;
    doPeaktf = 0;
  case 'Cancel'
    return;
end

currentsilent = DATA.Silent;
% %Create output matrix
outdata = cell(1,1); %+1 since header, 58 since header size

%Loop over all files
h = mywaitbarstart(numfiles,'Please wait, loading and summarizing files.',1);

% we need to do some roboclicking!
import java.awt.*;
import java.awt.event.*;
%Create a Robot-object to do the key-pressing
rob=Robot;

line=2;

for fileloop = 1:numfiles
  %--- Load file
  DATA.Silent = true; %Turn on "silent" mode to avoid to much update on screen when loading etc.
  disp(dprintf('Loading %s.',files2load(fileloop).name));

  SET = []; % %Make sure a fresh start
  load([pathname filesep files2load(fileloop).name],'-mat');
  %Assign
  if exist('setstruct','var')
    SET = setstruct;
    clear setstruct;
  else
    % there is no setstruct -> continue to the next loop iteration
    disp(['Could not open: ', files2load(fileloop).name])
    continue
  end

  openfile('setupstacksfrommat',1);
  segment('renderstacksfrommat');

  %For long axis its sufficient to consider one of the images to obtain
  %all information. therefore checked exists
  checked=[];
  bullseyeradial = cell(1,1);
  bullseyecirc = cell(1,1);
  for no=1:length(SET)

    if doTaggroup && isfield(SET(no),'StrainTagging') && not(isempty(SET(no).StrainTagging))

      switch SET(no).ImageViewPlane
        case 'Short-axis'
          imageviewplane = 'shortaxis';
        case {'2CH','3CH','4CH'}
          imageviewplane = 'longaxis';
        otherwise
          disp('Unknown image view plane');
          imageviewplane = [];
