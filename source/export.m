%-------------------------------------------
%Creates summary of multiple matfiles in one folder
%This function is very useful for research. The user
%performs all delineations and then exports all data to
%one spread sheet.
global DATA SET

if nargin < 1
  dosegdicom = false;
end

%---Make sure workspace is cleared and ask user to save current file
docancel = asktosavecurrentfile;
if docancel
  return
end

%---Select path
if dosegdicom
  suffix = 'segdicom';
else
  suffix = 'mat';
end
pathname = DATA.Pref.datapath;
pathname = myuigetdir(pathname,sprintf('Select a folder with .%s files',suffix));
if isequal(pathname,0)
  return
end

%---Find files to process
files2load = dir([pathname filesep sprintf('*.%s',suffix)]);
numfiles = length(files2load);
if numfiles == 0
  myfailed('No files found.',DATA.GUI.Segment);
  return
end

%---Store the user's preferences
silentstate = DATA.Silent;

%--- Write header
outdata{1, 1} = 'FileName';
outdata{1, 2} = 'PatientName';
outdata{1, 3} = 'PatientID';
outdata{1, 4} = 'AcquisitionDate';
outdata{1, 5} = 'Stack Number';
outdata{1, 6} = 'Image Type';
outdata{1, 7} = 'Image View Plane';
outdata{1, 8} = 'RoiName';
outdata{1, 9} = 'Mean Area';
outdata{1,10} = 'Min Area';
outdata{1,11} = 'Max Area';
outdata{1,12} = 'Mean';
outdata{1,13} = 'StD';
outdata{1,14} = 'Flow';

%---Loop over all files
h = mywaitbarstart(numfiles,'Please wait, loading and summarizing files.',1);
numrows = 1;
for fileloop = 1:numfiles
  %Load file
  DATA.Silent = true;
  filename = files2load(fileloop).name;
  fprintf('Loading %s.\n',filename);

  %---- try
  % This try-catch clause used to be commented out, which caused errors
  % in one file to crash the whole export process. Reintroducing it. /NL
  try
    %Load
    SET = []; %Make sure a fresh start
    if ~dosegdicom
      load([pathname filesep filename],'-mat');
    else
      loader = segloader();
      loader.adddicomfiles({fullfile(pathname,filename)});
      [~, setstruct] = loader.render(pathname, []);
      clear loader;
    end

    %Assign
    SET = setstruct;
    clear setstruct;

    %--- Loop over stacks and write data
    for no = 1:length(SET)
      Rois = SET(no).Roi;
      %We want them in alphabetical order
      for i = 1:length(Rois)
        names{i,1} = Rois.Name;
      end
      [~,sortind] = sortrows(names);
      names = {};
      Rois = Rois(sortind);
      if ~(length(Rois) == 1 && isempty(Rois(1).X))
        for roi = Rois
          outdata{numrows+1, 1} = filename;
          outdata{numrows+1, 2} = SET(no).PatientInfo.Name;
          outdata{numrows+1, 3} = SET(no).PatientInfo.ID;
          outdata{numrows+1, 4} = SET(no).PatientInfo.AcquisitionDate;
          outdata{numrows+1, 5} = no;
          outdata{numrows+1, 6} = SET(no).ImageType;
          outdata{numrows+1, 7} = SET(no).ImageViewPlane;
          outdata{numrows+1, 8} = roi.Name;
          outdata{numrows+1, 9} = nanmean(roi.Area);
          outdata{numrows+1,10} = min(roi.Area);
          outdata{numrows+1,11} = max(roi.Area);
          outdata{numrows+1,12} = nanmean(roi.Mean);
          outdata{numrows+1,13} = nanmean(roi.StD);
          if isfield(roi, 'Flow') && ~isempty(roi.Flow)
            outdata{numrows+1,14} = nanmean(roi.Flow.meanflow);
          end
          numrows = numrows + 1;
        end
      end
    end

  catch me
    %--- Something went wrong
    mydispexception(me);
    fprintf('Failed exporting data from %s.\n',filename);
    outdata{numrows,1} = filename;
    outdata{numrows,2} = 'FAILED.';
    numrows = numrows + 1;
  end

  h = mywaitbarupdate(h);

  %---Make sure to clear the workspace
  segment('filecloseall_Callback',true);

end %end of loop over files

%--- Output to a string
mywaitbarclose(h);
segment('cell2clipboard',outdata);

%---Revert to user's preferences
DATA.Silent = silentstate;

